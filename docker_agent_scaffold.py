def _agent_meta(agent_id: str, scaffold_type: str, spec: Dict[str, Any]) -> Dict[str, Any]:
    """Generate agent.json compatible with list_scaffolded_agents()."""
    root_model = spec.get("model") or spec.get("root_agent", {}).get("model", "dmr/ai/gemma3")
    sub_agents = spec.get("sub_agents", [])
    return {
        "agent_id": agent_id,
        "agent_type": spec.get("agent_type", "docker_ceagent"),
        "domain": spec.get("domain", ""),
        "scaffold_type": scaffold_type,  # "single_docker" | "multi_docker"
        "runtime": "ceagent",
        "model": root_model,
        "created_at": datetime.now(timezone.utc).isoformat(),
        "sub_agents": [sa.get("agent_id") for sa in sub_agents],
        "analysis_summary": {
            "complexity_level": spec.get("complexity_level"),
            "interaction_type": spec.get("interaction_type"),
            "problem_type": spec.get("problem_type"),
            "autonomy_level": spec.get("autonomy_level"),
        },
    }


# ── Public API ───────────────────────────────────────────────────────────────

def create_single_docker_agent(spec: Dict[str, Any]) -> Dict[str, Any]:
    """
    Scaffold a complete single-agent Ceagent v2 Docker project.

    spec keys:
      agent_id    : str   — folder name and image slug
      model       : str   — e.g. "dmr/ai/gemma3", "openai/gpt-4o"
      instruction : str   — agent system prompt / instruction
      toolsets    : list  — list of {"type":"builtin","name":"thinking"} dicts
      port        : int   — host port (defaults to next free port from 8301)
      domain      : str   — optional domain label
      complexity_level, interaction_type, autonomy_level, problem_type : optional

    Returns: {agent_id, path, files_created, ceagent_yaml, port}
    """
    agent_id = spec.get("agent_id", "agent")
    port = _safe_port(spec.get("port", _SINGLE_PORT_START), _SINGLE_PORT_START)
    model = spec.get("model", "dmr/ai/gemma3")

    agent_dir = AGENTS_DIR / agent_id
    agent_dir.mkdir(parents=True, exist_ok=True)

    ceagent_yaml = _ceagent_single_yaml(spec)
    files: List[str] = []

    def _write(name: str, content: str) -> None:
        (agent_dir / name).write_text(content)
        files.append(str(agent_dir / name))

    _write("ceagent.yaml", ceagent_yaml)
    _write("Dockerfile", _dockerfile(agent_id))
    _write("docker-compose.yml", _compose_single(agent_id, port, [model]))
    _write(".env.example", _env_example(_detect_providers([model])))
    _write("README.md", _readme_single(agent_id, spec, port))
    _write("agent.json", json.dumps(_agent_meta(agent_id, "single_docker", spec), indent=2) + "\n")

    return {
        "agent_id": agent_id,
        "path": str(agent_dir),
        "files_created": files,
        "ceagent_yaml": ceagent_yaml,
        "port": port,
        "scaffold_type": "single_docker",
        "runtime": "ceagent",
    }


def create_multi_docker_agent(spec: Dict[str, Any]) -> Dict[str, Any]:
    """
    Scaffold a multi-agent Ceagent v2 Docker project (root + sub-agents).

    spec keys:
      agent_id    : str   — folder name and image slug for the stack
      root_agent  : dict  — {model, instruction, toolsets}
      sub_agents  : list  — [{agent_id, model, instruction, description, toolsets}, ...]
      port        : int   — host port (defaults to next free from 8401)
      domain, complexity_level, etc. — optional labels

    Returns: {agent_id, path, files_created, ceagent_yaml, port, sub_agents}
    """
    agent_id = spec.get("agent_id", "multi_agent_stack")
    port = _safe_port(spec.get("port", _MULTI_PORT_START), _MULTI_PORT_START)

    root = spec.get("root_agent", {})
    sub_agents: List[Dict[str, Any]] = spec.get("sub_agents", [])

    all_models = [root.get("model", "dmr/ai/gemma3")]
    for sa in sub_agents:
        all_models.append(sa.get("model", "dmr/ai/gemma3"))

    sub_ids = [sa.get("agent_id", f"sub_{i}") for i, sa in enumerate(sub_agents)]

    agent_dir = AGENTS_DIR / agent_id
    agent_dir.mkdir(parents=True, exist_ok=True)

    ceagent_yaml = _ceagent_multi_yaml(spec)
    files: List[str] = []

    def _write(name: str, content: str) -> None:
        (agent_dir / name).write_text(content)
        files.append(str(agent_dir / name))

    _write("ceagent.yaml", ceagent_yaml)
    _write("Dockerfile", _dockerfile(agent_id))
    _write("docker-compose.yml", _compose_multi(agent_id, port, sub_ids, all_models))
    _write(".env.example", _env_example(_detect_providers(all_models)))
    _write("README.md", _readme_multi(agent_id, spec, port))
    _write("agent.json", json.dumps(_agent_meta(agent_id, "multi_docker", spec), indent=2) + "\n")

    return {
        "agent_id": agent_id,
        "path": str(agent_dir),
        "files_created": files,
        "ceagent_yaml": ceagent_yaml,
        "port": port,
        "scaffold_type": "multi_docker",
        "runtime": "ceagent",
        "sub_agents": sub_ids,
    }
